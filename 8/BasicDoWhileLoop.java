public class BasicDoWhileLoop {
    public static void main(String[] args) {
        int i = 1;
        
        System.out.println("นับเลข 1 ถึง 5:");
        do {
            System.out.println(i);
            i++;
        } while (i <= 5);
        
        System.out.println("จบการนับ");
    }
}
