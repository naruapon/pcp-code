public class PrintfBasic {
    public static void main(String[] args) {
        String name = "ณัฐพล";
        int age = 22;
        double height = 178.5;
        
        // แสดงผลแบบพื้นฐาน
        System.out.printf("ชื่อ: %s%n", name);
        System.out.printf("อายุ: %d ปี%n", age);
        System.out.printf("ส่วนสูง: %f ซม.%n", height);
    }
}
