public class NewlineExample {
    public static void main(String[] args) {
        // ใช้ \n ขึ้นบรรทัดใหม่
        System.out.print("บรรทัดที่ 1\nบรรทัดที่ 2\nบรรทัดที่ 3");
        
        System.out.println("\n"); // บรรทัดว่าง
        
        // ตัวอย่างเนื้อหาหลายบรรทัด
        System.out.print("ชื่อ: นฤพล\nอายุ: 20 ปี\nเมือง: กรุงเทพฯ");
    }
}
